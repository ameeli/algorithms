import unittest


def find_rectangular_overlap(rect1, rect2):
    """Find intersection of 2 rectangles."""
    rect1_right_x = rect1['left_x'] + rect1['width']
    rect1_top_y = rect1['bottom_y'] + rect1['height']

    rect2_right_x = rect2['left_x'] + rect2['width']
    rect2_top_y = rect2['bottom_y'] + rect2['height']

    new_rect_left_x = max(rect1['left_x'], rect2['left_x'])
    new_rect_right_x = min(rect1_right_x, rect2_right_x)
    new_rect_bottom_y = max(rect1['bottom_y'], rect2['bottom_y'])
    new_rect_top_y = min(rect1_top_y, rect2_top_y)

    new_rect_width = new_rect_right_x - new_rect_left_x
    new_rect_height = new_rect_top_y - new_rect_bottom_y

    if (new_rect_width <= 0) or (new_rect_height <= 0):
        return {'left_x': None,
                'bottom_y': None,
                'width': None,
                'height': None}

    return {'left_x': new_rect_left_x,
            'bottom_y': new_rect_bottom_y,
            'width': new_rect_width,
            'height': new_rect_height}


class Test(unittest.TestCase):

    def test_overlap_along_both_axes(self):
        rect1 = {
            'left_x': 1,
            'bottom_y': 1,
            'width': 6,
            'height': 3,
        }
        rect2 = {
            'left_x': 5,
            'bottom_y': 2,
            'width': 3,
            'height': 6,
        }
        expected = {
            'left_x': 5,
            'bottom_y': 2,
            'width': 2,
            'height': 2,
        }
        actual = find_rectangular_overlap(rect1, rect2)
        self.assertEqual(actual, expected)


